export function getItemsByType(type: string): Item[] {
  return enhancedItems.filter((item) => item.type === type);
}

export function getLinksForItem(itemId: string): {
  incoming: Link[];
  outgoing: Link[];
} {
  return {
    incoming: enhancedLinks.filter((link) => link.targetId === itemId),
    outgoing: enhancedLinks.filter((link) => link.sourceId === itemId),
  };
}

export function getChildItems(parentId: string): Item[] {
  return enhancedItems.filter((item) => item.parentId === parentId);
}

export function getItemPath(itemId: string): string[] {
  const path: string[] = [];
  let current = enhancedItems.find((item) => item.id === itemId);

  while (current) {
    path.unshift(current.id);
    current = current.parentId
      ? enhancedItems.find((item) => item.id === current!.parentId)
      : undefined;
  }

  return path;
}
